package codeup100;

import java.util.Scanner;

public class CodeUp1071 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		//반복문 쓰지 말랬는디 안쓰고는 도저히 방법을 몰겄다잉~
		while(true) {
			int input = sc.nextInt();
			if(input == 0)
				return;
			else
				System.out.println(input);
			
		}

	}

}
